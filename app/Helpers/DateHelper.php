<?php

namespace App\Helpers;

class DateHelper
{
    public static function formatIndonesianDate($date)
    {
        $days = [
            'Sunday' => 'Minggu',
            'Monday' => 'Senin',
            'Tuesday' => 'Selasa',
            'Wednesday' => 'Rabu',
            'Thursday' => 'Kamis',
            'Friday' => 'Jumat',
            'Saturday' => 'Sabtu'
        ];

        $months = [
            'January' => 'Januari',
            'February' => 'Februari',
            'March' => 'Maret',
            'April' => 'April',
            'May' => 'Mei',
            'June' => 'Juni',
            'July' => 'Juli',
            'August' => 'Agustus',
            'September' => 'September',
            'October' => 'Oktober',
            'November' => 'November',
            'December' => 'Desember'
        ];

        $timestamp = strtotime($date);
        $dayName = $days[date('l', $timestamp)];
        $day = date('j', $timestamp);
        $monthName = $months[date('F', $timestamp)];
        $year = date('Y', $timestamp);
        return "$dayName $day $monthName $year";
    }
}
